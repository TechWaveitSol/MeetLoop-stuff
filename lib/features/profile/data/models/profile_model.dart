import 'package:cloud_firestore/cloud_firestore.dart';
import '../../domain/entities/profile_entity.dart';

class ProfileModel extends ProfileEntity {
  const ProfileModel({
    required String uid,
    required String displayName,
    String username = '',
    String bio = '',
    String gender = '',
    String pronouns = '',
    DateTime? dateOfBirth,
    bool isAgeVerified = false,
    double? height,
    List<String> languages = const [],
    String relationshipStatus = 'Single',
    String occupation = '',
    String education = '',
    String religion = '',
    String smokingPreference = 'No',
    String drinkingPreference = 'No',
    List<String> pets = const [],
    List<String> hobbies = const [],
    List<String> interests = const [],
    List<String> favoriteMusic = const [],
    List<String> favoriteMovies = const [],
    List<String> sports = const [],
    List<String> photos = const [],
    String? profileVideoUrl,
    bool verificationBadge = false,
    String personalityType = '',
    Map<String, dynamic> privacySettings = const {},
    Map<String, dynamic> notificationSettings = const {},
    Map<String, dynamic> locationSettings = const {},
    List<String> blockedUsers = const [],
    List<String> mutedUsers = const [],
    List<String> friends = const [],
    int profileCompletionPercentage = 0,
  }) : super(
          uid: uid,
          displayName: displayName,
          username: username,
          bio: bio,
          gender: gender,
          pronouns: pronouns,
          dateOfBirth: dateOfBirth,
          isAgeVerified: isAgeVerified,
          height: height,
          languages: languages,
          relationshipStatus: relationshipStatus,
          occupation: occupation,
          education: education,
          religion: religion,
          smokingPreference: smokingPreference,
          drinkingPreference: drinkingPreference,
          pets: pets,
          hobbies: hobbies,
          interests: interests,
          favoriteMusic: favoriteMusic,
          favoriteMovies: favoriteMovies,
          sports: sports,
          photos: photos,
          profileVideoUrl: profileVideoUrl,
          verificationBadge: verificationBadge,
          personalityType: personalityType,
          privacySettings: privacySettings,
          notificationSettings: notificationSettings,
          locationSettings: locationSettings,
          blockedUsers: blockedUsers,
          mutedUsers: mutedUsers,
          friends: friends,
          profileCompletionPercentage: profileCompletionPercentage,
        );

  factory ProfileModel.fromJson(Map<String, dynamic> json) {
    DateTime? dob;
    if (json['dateOfBirth'] != null) {
      if (json['dateOfBirth'] is Timestamp) {
        dob = (json['dateOfBirth'] as Timestamp).toDate();
      } else if (json['dateOfBirth'] is String) {
        dob = DateTime.parse(json['dateOfBirth'] as String);
      }
    }

    return ProfileModel(
      uid: json['uid'] as String? ?? '',
      displayName: json['displayName'] as String? ?? '',
      username: json['username'] as String? ?? '',
      bio: json['bio'] as String? ?? '',
      gender: json['gender'] as String? ?? '',
      pronouns: json['pronouns'] as String? ?? '',
      dateOfBirth: dob,
      isAgeVerified: json['isAgeVerified'] as bool? ?? false,
      height: (json['height'] as num?)?.toDouble(),
      languages: List<String>.from(json['languages'] ?? []),
      relationshipStatus: json['relationshipStatus'] as String? ?? 'Single',
      occupation: json['occupation'] as String? ?? '',
      education: json['education'] as String? ?? '',
      religion: json['religion'] as String? ?? '',
      smokingPreference: json['smokingPreference'] as String? ?? 'No',
      drinkingPreference: json['drinkingPreference'] as String? ?? 'No',
      pets: List<String>.from(json['pets'] ?? []),
      hobbies: List<String>.from(json['hobbies'] ?? []),
      interests: List<String>.from(json['interests'] ?? []),
      favoriteMusic: List<String>.from(json['favoriteMusic'] ?? []),
      favoriteMovies: List<String>.from(json['favoriteMovies'] ?? []),
      sports: List<String>.from(json['sports'] ?? []),
      photos: List<String>.from(json['photos'] ?? []),
      profileVideoUrl: json['profileVideoUrl'] as String?,
      verificationBadge: json['verificationBadge'] as bool? ?? false,
      personalityType: json['personalityType'] as String? ?? '',
      privacySettings: Map<String, dynamic>.from(json['privacySettings'] ?? {}),
      notificationSettings: Map<String, dynamic>.from(json['notificationSettings'] ?? {}),
      locationSettings: Map<String, dynamic>.from(json['locationSettings'] ?? {}),
      blockedUsers: List<String>.from(json['blockedUsers'] ?? []),
      mutedUsers: List<String>.from(json['mutedUsers'] ?? []),
      friends: List<String>.from(json['friends'] ?? []),
      profileCompletionPercentage: json['profileCompletionPercentage'] as int? ?? 0,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'uid': uid,
      'displayName': displayName,
      'username': username,
      'bio': bio,
      'gender': gender,
      'pronouns': pronouns,
      'dateOfBirth': dateOfBirth?.toIso8601String(),
      'isAgeVerified': isAgeVerified,
      'height': height,
      'languages': languages,
      'relationshipStatus': relationshipStatus,
      'occupation': occupation,
      'education': education,
      'religion': religion,
      'smokingPreference': smokingPreference,
      'drinkingPreference': drinkingPreference,
      'pets': pets,
      'hobbies': hobbies,
      'interests': interests,
      'favoriteMusic': favoriteMusic,
      'favoriteMovies': favoriteMovies,
      'sports': sports,
      'photos': photos,
      'profileVideoUrl': profileVideoUrl,
      'verificationBadge': verificationBadge,
      'personalityType': personalityType,
      'privacySettings': privacySettings,
      'notificationSettings': notificationSettings,
      'locationSettings': locationSettings,
      'blockedUsers': blockedUsers,
      'mutedUsers': mutedUsers,
      'friends': friends,
      'profileCompletionPercentage': profileCompletionPercentage,
    };
  }
}
